import math

def area(r):
    '''Функция для нахождения площади круга.'''
    return math.pi * r * r

def perimeter(r):
    '''Функция для нахождения периметра круга.'''
    return 2 * math.pi * r

# Пример:
# Входные данные: r = 3
# 1. def area(r)
# Возвращаемое значение: 28.274333882308138
# 2. def perimeter(r)
# Возвращаемое значение: 18.84955592153876
